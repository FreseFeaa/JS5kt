const test = require("node:test");
const assert = require("node:assert");

const { maskCard } = require("./02.js");

test("Заменить на * все цифры идущие после первых 4 и до послдених 4", () => {
    const cards = [
        '4000 0012 0056 9499',
        '4000 0013 5456 7379',
        '4000 0014 1456 9869',
        '4000 0015 3466 7859',
        '4000 0016 3556 6899',
        '4000 0017 4456 4699',
      ];
      const cards2 = [
        '5093 1111 6969 0000',
        '5093 2222 9898 1234',
        '5093 3333 6868 5678',
        '5093 4444 9898 1357',
        '5093 5555 0505 2468',
        '5093 6666 4949 0216',
        '5093 7777 9393 7859',
        '5093 8888 8282 9876',
      ];



    assert.deepStrictEqual(cards.map(maskCard), [
  '4000***9499',
  '4000***7379',
  '4000***9869',
  '4000***7859',
  '4000***6899',
  '4000***4699'
]);
    assert.deepStrictEqual(cards2.map(maskCard), [
        '5093***0000',
        '5093***1234',
        '5093***5678',
        '5093***1357',
        '5093***2468',
        '5093***0216',
        '5093***7859',
        '5093***9876',
      ]);   


});
